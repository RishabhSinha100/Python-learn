num1 = 11
num2 = 99

print((num2 == num1), "This checks Equality")            # False
print((num2 != num1), "This checks Not Equal")           # True
print((num2 > num1), "This checks Greater Than")         # True
print((num2 < num1), "This checks Less Than")            # False
print((num2 >= num1), "This checks Greater Than or Equal")  # True
print((num2 <= num1), "This checks Less Than or Equal")     # False
